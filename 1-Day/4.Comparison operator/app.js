// comparison operator
// -> realtional operator
// -> equality operator

// relational operator
// >, <, >=, <=, !=, ==
// > : greater than
// < : less than
// >= : greater than or equal to
// <= : less than or equal to
// != : not equal to
// == : equal to
console.log(10 < 12);
console.log(10 > 12);

// equality operator
//  === strictly equal to
//  !== not strictly equal to
console.log(10 === 10);
console.log(10 !== 10);
